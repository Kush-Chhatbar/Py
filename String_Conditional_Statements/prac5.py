"""
String related functions

"""
#endswith() function
str = "My name is Kush Chhatbar."
print(str.endswith("bar."))

#capitalize() function
str1 = "kush chhatbar."
print(str1.capitalize())

#replace() function
str2 = "Kush Chhatbar"
print(str2.replace('Kush', 'K'))

#find() function
str3 = "Kush Chhatbar"
print(str3.find('h'))
print(str3.find('a'))

#count() function
str4 = "Kush Chhatbar."
print(str4.count('h'))